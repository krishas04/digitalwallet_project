def _add_round_key(state, key):
    return [s ^ k for s, k in zip(state, key)]

def _key_expansion(key):
    Nk = 8
    key_words = [key[i:i+4] for i in range(0, 32, 4)]
    for i in range(Nk, 60):
        temp = list(key_words[i-1])
        if i % Nk == 0:
            temp = temp[1:] + temp[:1]
            temp = [S_BOX[b] for b in temp]
            temp[0] ^= RCON[i//Nk]
        elif i % Nk == 4:
            temp = [S_BOX[b] for b in temp]
        prev_key_word = key_words[i-Nk]
        new_word = bytes([b1 ^ b2 for b1, b2 in zip(prev_key_word, temp)])
        key_words.append(new_word)
    return [b for word in key_words for b in word]

def _pad(data):
    padding_len = 16 - (len(data) % 16)
    padding = bytes([padding_len] * padding_len)
    return data + padding

# --- NEW: INVERSE (DECRYPTION) HELPER FUNCTIONS ---
def _unpad(data):
    padding_len = data[-1]
    if padding_len > 16: return data # Not padded
    return data[:-padding_len]

def _inv_sub_bytes(state):
    return [INV_S_BOX[b] for b in state]

def _inv_shift_rows(state):
    new_state = state[:]
    new_state[5], new_state[9], new_state[13], new_state[1] = state[1], state[5], state[9], state[13]
    new_state[10], new_state[14], new_state[2], new_state[6] = state[2], state[6], state[10], state[14]
    new_state[15], new_state[3], new_state[7], new_state[11] = state[3], state[7], state[11], state[15]
    return new_state

def _inv_mix_columns(state):
    new_state = state[:]
    for i in range(4):
        col = state[i*4:i*4+4]
        new_state[i*4]   = _galois_mult(col[0], 0x0e) ^ _galois_mult(col[1], 0x0b) ^ _galois_mult(col[2], 0x0d) ^ _galois_mult(col[3], 0x09)
        new_state[i*4+1] = _galois_mult(col[0], 0x09) ^ _galois_mult(col[1], 0x0e) ^ _galois_mult(col[2], 0x0b) ^ _galois_mult(col[3], 0x0d)
        new_state[i*4+2] = _galois_mult(col[0], 0x0d) ^ _galois_mult(col[1], 0x09) ^ _galois_mult(col[2], 0x0e) ^ _galois_mult(col[3], 0x0b)
        new_state[i*4+3] = _galois_mult(col[0], 0x0b) ^ _galois_mult(col[1], 0x0d) ^ _galois_mult(col[2], 0x09) ^ _galois_mult(col[3], 0x0e)
    return new_state

# --- Main Encryption Function ---
def encrypt(plaintext_bytes, key_bytes):
    round_keys = _key_expansion(key_bytes)
    padded_plaintext = _pad(plaintext_bytes)
    ciphertext = b''   # an empty byte variable created
    for i in range(0, len(padded_plaintext), 16):
        block = list(padded_plaintext[i:i+16])
        state = _add_round_key(block, round_keys[0:16])
        for round_num in range(1, 14):
            state = _sub_bytes(state)
            state = _shift_rows(state)
            state = _mix_columns(state)
            state = _add_round_key(state, round_keys[round_num*16 : (round_num+1)*16])
        state = _sub_bytes(state)
        state = _shift_rows(state)
        state = _add_round_key(state, round_keys[14*16 : 15*16])
        ciphertext += bytes(state)
    return ciphertext

# --- NEW: Main Decryption Function ---
def decrypt(ciphertext_bytes, key_bytes):
    round_keys = _key_expansion(key_bytes)
    decrypted_data = b''
    for i in range(0, len(ciphertext_bytes), 16):
        block = list(ciphertext_bytes[i:i+16])
        state = _add_round_key(block, round_keys[14*16 : 15*16])
        state = _inv_shift_rows(state)
        state = _inv_sub_bytes(state)
        for round_num in range(13, 0, -1):
            state = _add_round_key(state, round_keys[round_num*16 : (round_num+1)*16])
            state = _inv_mix_columns(state)
            state = _inv_shift_rows(state)
            state = _inv_sub_bytes(state)
        state = _add_round_key(state, round_keys[0:16])
        decrypted_data += bytes(state)
    return _unpad(decrypted_data)